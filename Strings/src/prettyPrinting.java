public class prettyPrinting {
    public static void main(String[] args) {
        float a = 453.123454f;
        // we have to print till 2 decimal values
        System.out.printf("Formatted number is %.2f",a);
        System.out.print("\n");
        System.out.printf("%.3f",Math.PI);
        System.out.print("\n");
        System.out.printf("Hello my name is %s and i am %s","Dipit","cool");
    }
}
