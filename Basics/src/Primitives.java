public class Primitives {
    public static void main(String[] args){
        // Data types that cannot be broken down further are referred as Primitive data types
        // Integer
        int roll_no = 12;
        System.out.println(roll_no);
        // String - not primitive
        String name = "Dipit";
        System.out.println(name);

        // character
        char grade = 'A';
        System.out.println(grade);

        // float - to store decimal numbers
        float marks = 98.12f;
        System.out.println(marks);

        // double - to store large float numbers
        double bill = 689877.232;
        System.out.println("Your bill is" + bill);

        // long - to store large integers
        long rank = 12321;
        System.out.println("Your rank is " + rank);

        // boolean - true/false
        boolean result_out = true;
        System.out.println(result_out);
    }


}
